	private int[] tabPvBlock = new int[carte1.length];  
	
	public Map () {
		pvBlock(carte1);
	}
	
	public void pvBlock(int [] carte) {
		for(int i=0 ; i < carte.length-1 ; i++) {
			if(carte[i] == 4 || carte[i] == 5 || carte[i] == 6)
				tabPvBlock[i] = 5;
			else
				tabPvBlock[i] = 0;
		}
	}
	
	public void changementMap(int[] tab) {
		for(int i=0; i<carte1.length ; i++)
			carte1[i] = tab[i];
		pvBlock(tab);
	}
	
	public void ajouterResource(int i, int resource) {
		carte1[i] = resource;
		tabPvBlock[i] = 5;
	}

	public void removeResource() {
		for(int i=0 ; i<carte1.length ; i++) {
			if(carte1[i]== 4 || carte1[i]== 5 || carte1[i]== 6) {
				carte1[i]= 0;
				tabPvBlock[i] = 0;
			}
		}
	}
	
	
